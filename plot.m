clear
px = [1,3,6,9];
py = [1,4,4.5,0];

syms t
n = 3;
x(t) = px(1) * (1-t)^3 +...
    px(2) * nchoosek(3,1) * t * (1-t)^2 + ...
    px(3) * nchoosek(3,2) * t^2 * (1-t) + ...
    px(4) * t^3;

y(t) = py(1) * (1-t)^3 +...
    py(2) * nchoosek(3,1) * t * (1-t)^2 + ...
    py(3) * nchoosek(3,2) * t^2 * (1-t) + ...
    py(4) * t^3;
plot(px,py, '*--');
hold on;
axis equal;
fplot(x,y,[0 1]);
